/*
STEPS FOR PASSING COMMAND LINE ARGS IN NETBEANS

to pass the command line arguments from netbeans

1) goto run ->set project conf->customize
2) in the run option 
	1) set the Main-Class
	2) set the arguments
	click on ok.
3) click on run-project (instead of run file)
	execution will start for the class which you configured
	as main-class
	arguments configured will be passed to parameter of
	main method of main-class.
*/